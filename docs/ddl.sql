COMMENT ON COLUMN TB_CUST_INFO.PAID_CHNL_CNT_GRP IS '유료 채널(캐치온 등) 가입 수';
COMMENT ON COLUMN TB_CUST_INFO.SCRB_PATH_NM_GRP IS '고객 유입 채널';
