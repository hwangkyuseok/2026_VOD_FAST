    SRL_NO              BIGSERIAL PRIMARY KEY,
    PLATFORM            TEXT NOT NULL,          -- HELLOVISION, COUPANG, NAVER
    IS_RENTAL           CHAR(1) DEFAULT 'N',    -- 렌탈 여부 (Y/N)
    PROD_CD             TEXT NOT NULL,          -- 각 사이트의 고유 상품번호
    PROD_NM             TEXT NOT NULL,          -- 상품명
